def seed_database():
    init_db()
    db = get_db()
    cur = db.cursor()

    # Insert teams
    team_ids = {}
    for i, (name, group, flag) in enumerate(TEAMS, 1):
        cur.execute('INSERT INTO teams (name, group_name, flag_emoji) VALUES (?, ?, ?)', (name, group, flag))
        team_ids[i] = cur.lastrowid

    # Insert players (26 per team from real WC squads)
    player_count = 0
    for team_idx in range(1, 49):
        team_id = team_ids[team_idx]
        cn_name = TEAMS[team_idx - 1][0]
        squad = PLAYERS_DATA.get(cn_name, [])
        for p in squad:
            name, name_cn, jersey_number, position = p
            cur.execute('INSERT INTO players (name, name_cn, jersey_number, team_id, position) VALUES (?, ?, ?, ?, ?)', (name, name_cn, jersey_number, team_id, position))
            player_count += 1

    # Insert participants
    participants = [
        ('耗子', 1),
        ('庆爷', 2),
        ('李总', 3),
        ('老闫', 4),
        ('老王', 5),
    ]
    for name, order in participants:
        cur.execute('INSERT INTO participants (name, draft_order) VALUES (?, ?)', (name, order))

    db.commit()
    db.close()
    print(f'Seeded: {len(TEAMS)} teams, {player_count} players, {len(participants)} participants')


def load_team_ownership():
    """Read team ownership JSON; validate 5 owners x 8 teams."""
    if os.path.isfile(TEAM_OWNERSHIP_JSON):
        path = TEAM_OWNERSHIP_JSON
    elif os.path.isfile(TEAM_INFO_PATH):
        path = TEAM_INFO_PATH
    else:
        raise FileNotFoundError(
            f'Missing team ownership: {TEAM_OWNERSHIP_JSON} or {TEAM_INFO_PATH}'
        )
    with open(path, encoding='utf-8') as f:
        data = json.load(f)
    if set(data.keys()) != set(PARTICIPANT_NAMES):
        raise ValueError(f'teamInfo keys must be exactly {PARTICIPANT_NAMES}')
    seen = set()
    team_names = {t[0] for t in TEAMS}
    for owner, teams in data.items():
        if len(teams) != TEAMS_PER_OWNER:
            raise ValueError(f'{owner} must have {TEAMS_PER_OWNER} teams, got {len(teams)}')
        for name in teams:
            if name not in team_names:
                raise ValueError(f'Unknown team: {name}')
            if name in seen:
                raise ValueError(f'Duplicate team assignment: {name}')
            seen.add(name)
    if len(seen) != 40:
        raise ValueError(f'Expected 40 assigned teams, got {len(seen)}')
    return data


def sync_team_ownership():
    """Sync team_ownership table only; never touches selections."""
    ownership = load_team_ownership()
    db = get_db()
    db.execute('DELETE FROM team_ownership')
    for owner_name, team_names in ownership.items():
        prow = db.execute('SELECT id FROM participants WHERE name = ?', (owner_name,)).fetchone()
        if not prow:
            db.close()
            raise ValueError(f'Participant not found: {owner_name}')
        participant_id = prow['id']
        for team_name in team_names:
            trow = db.execute('SELECT id FROM teams WHERE name = ?', (team_name,)).fetchone()
            if not trow:
                db.close()
                raise ValueError(f'Team not found: {team_name}')
            db.execute(
                'INSERT INTO team_ownership (team_id, participant_id) VALUES (?, ?)',
                (trow['id'], participant_id),
            )
    db.commit()
    db.close()


